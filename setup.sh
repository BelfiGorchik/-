#!/bin/bash

# Установка кодировки UTF-8
export LANG=en_US.UTF-8

echo "======================================================================="
echo "         АНАЛИЗАТОР ВОРОНКИ НАЙМА (Recruitment Funnel Analyzer)"
echo "                     Выпускная квалификационная работа"
echo "======================================================================="
echo ""

echo "[1/4] Проверка установленной языковой платформы Node.js..."
if ! command -v node &> /dev/null
then
    echo "[ОШИБКА] Платформа Node.js не установлена на данном компьютере!"
    echo "Пожалуйста, скачайте и установите Node.js (версии 18 или выше) c официального сайта:"
    echo "https://nodejs.org/"
    echo "После установки перезапустите этот скрипт."
    echo ""
    exit 1
fi

NODE_VER=$(node -v)
echo "Найдена платформа Node.js версии: $NODE_VER"
echo ""

echo "[2/4] Проверка конфигурационных параметров среды (.env)..."
if [ ! -f .env ]; then
    cp .env.example .env
    echo "Конфигурационный файл .env создан автоматически."
else
    echo "Конфигурационный файл .env уже подготовлен. Пропускаем."
fi
echo ""

echo "[3/4] Установка системных npm-зависимостей..."
echo "Пожалуйста, подождите, это может занять некоторое время..."
npm install
if [ $? -ne 0 ]; then
    echo "[ОШИБКА] Не удалось завершить установку необходимых пакетов. Проверьте подключение к сети."
    exit 1
fi
echo "Пакеты успешно загружены и установлены."
echo ""

echo "[4/4] Запуск локального сервера бизнес-логики и дашборда..."
echo ""
echo "Локальный адрес дашборда по умолчанию: http://localhost:3000"
echo ""
echo "======================================================================="
echo " Служба Node.js автоматически подберет свободный порт при конфликте!"
echo " Веб-браузер откроется автоматически СРАЗУ после готовности сервера."
echo "======================================================================="
echo ""

npm run dev
